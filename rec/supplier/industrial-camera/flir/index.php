<article id="product-polarized-camera">
<h2 class="ttl-navy">偏光カメラ</h2>
<p class="lead">Sonyが開発した偏光フィルム搭載CMOSセンサ（IMX250MZR）を用い、29㎜角の偏光カメラとして世界に先駆けてリリースいたしました。このCMOSセンサは45°刻みの偏光フィルムをCMOSセンサの画素に直接貼り付けをされており、従来の偏光カメラよりも安価にご使用いただけます。<br>
偏光カメラを使うことで、困難であったキズの可視化や反射光の除去などへの可能性が広がります。</p>
<p class="product"><img src="./img/polarized-camera-product.jpg" alt="USB VISION / GiGE VISION"></p>

<div class="sensor">
