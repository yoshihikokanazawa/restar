<article id="kihan-fundamental">
<h2 class="ttl-navy">基本姿勢</h2>

<ul class="detail">
<li>( 1 ) 関係法令および社内規程並びに社会規範を遵守します。</li>
<li>( 2 ) ステークホルダーの人権、個性、人格および多様性を尊重します。</li>
<li>( 3 ) ステークホルダーとの公平かつ透明な関係を維持し、公正な取引を行います。</li>
<li>( 4 ) ステークホルダーとの誠実なコミュニケーションを推進し、迅速、正確かつ公平な情報公開を行います。</li>
<li>( 5 ) 社会の一員として、事業活動や社会貢献活動を通じて環境問題への取組みを積極的に推進します。</li>
<li>( 6 ) 常に自己研鑽に努め、先導的なビジネスの創造への挑戦を継続します。</li>
<li>( 7 ) 顧客ニーズに合致した最適な商品とサービスを提供します。</li>
</ul>
