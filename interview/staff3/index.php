<section>
<h2 class="ttl-blue-underline">大学時代に養った「人や部門をつなぐ能力」をいかしたかったし、実際に役に立っている。</h2>
<p class="interview-txt">大学時代は「新歓実行委員会」に所属していました。大学を拠点に活動する部やサークルが一堂に集まって新入生を歓迎するイベントを企画運営する団体です。数多くのサークルと連携を取りながら、学校側が求める範囲内の勧誘活動にできるよう調整したり、サークル側から上がってくる企画を精査したりしていました。<br>
そのような活動をしているうちに、多くの人や部門と関わる仕事に就きたい、経営に近い部署で働きたいと思うようになり、経験がいかせそうな「管理部門」のある会社を探していて、レスターと出会うことができました。<br>
<br>
現在はRHD（レスターホールディングス）の広報・CSR・IR部で働いています。大きく広報とIRに分けられるのですが、広報部門は、ホームページの更新などの運営や、採用に関すること、他社への訪問に使う会社案内の作成、本社ショールーム内の展示物の調整などが主な業務です。IRは投資家に特化した動きとなりますので、法務や経理といった知識を持った人が中心となって動いています。<br>
<br>
私が所属している部署は、経理や人事、広報、経営企画などの管理部門と営業部との間に入って調整することが多く、そのあたりは学校側とサークルの間に入っていた大学時代と似ていると思っています。</p>
<p class="interview-photo"><img src="img/interview-photo1.jpg" alt=""></p>
</section>


<section>
<h2 class="ttl-blue-underline">自分で段取りを組み立てて進めることは、やりがいにつながっている。</h2>
<p class="interview-txt">業務の内容は、打ち合わせや電話対応、資料作成などが中心で、グループ会社へ行く以外は社外に出ることはほとんどありません。<br>
今はまだ新人ということもあり、様々な業務に対してお手伝いさせていただいているという状況ですが、ある意味マルチタスクな業務を経験しています。段取りを組んで、スケジュールに合わせて進めていくということは性に合っている気がするので、そういう意味でやりがいを感じています。<br>
<br>
レスターは統合したばかりということもあり、マニュアルというものがまだありません。ざっくりとした枠の中で応用を利かせられることが多々あります。もちろん先輩や上司に相談したうえでのことですが、自分で目標を立てて、段取りを組み立てられるということはやりがいにつながっています。レスターは挑戦のしがいのある会社だと私は思っています。<br>
<br>
反面、経営企画部と一緒に動いているため、経営側の声が聞こえてくる位置にいます。その一方で、グループ会社や実際に営業活動をしている人たちの声も聞こえてくる。双方の要望をしっかり聞いて、的確な判断をしていかなくてはならない部署だと感じています。そのうえで新しいこともやっていかなきゃいけない。そのためにはいろいろな意見を聞きながら、自分の考えもしっかりと持たないといけないと最近では痛感しています。</p>
<p class="interview-photo"><img src="img/interview-photo2.jpg" alt=""></p>
</section>


<section>
<h2 class="ttl-blue-underline">個性をいかした働き方ができる、それがレスターの強み。</h2>
<p class="interview-txt">レスターのユニークなところは、経営層の声が強くて割とトップダウン型だった会社と、現場からの声を拾ってそれを形にしていきましょうという、言ってみれば真逆の考えを持つ会社が統合したこと。トップダウンだからこそスピード感を持ってできる部分と、自分たちで調整する力があるという、両面が重なっているところが強みなのではないかと思っています。<br>
<br>
そういった二面性のある社風の中で、社員それぞれが個性をいかして働いているように思います。また、新人が働きやすい会社でもあると感じています。同期は特に個性の強い人が集まっているような印象がありますが、それぞれ先輩に受け入れられながら働いています。私はいろいろと喋りたい、意見したいというタイプなのですが（笑）、ときに偉そうだなとか生意気だなとか思われることもあるかもしれないですが、今のところそういうことも受け入れてくれています。<br>
<br>
強みというよりも個人的に好きな部分になりますが、会社がある場所もいいですね。主要駅である品川駅から外れた東品川という場所で、オフィス街でもないですが、逆に落ち着いた環境の中で心穏やかに仕事ができるのではないかと思います。あとは会社の保養所が静岡・伊東にあるのですが、とても素敵なところです。</p>
<p class="interview-photo"><img src="img/interview-photo3.jpg" alt=""></p>
</section>


<section>
<h2 class="ttl-blue-underline">グループ会社との関係性づくりを強めたい。</h2>
<p class="interview-txt">業務の上で目標としているのは、グループ会社との関係作りです。部署内だけで過ごしていると、どうしても他部署や他のグループ会社の人との関係が薄くなっていく気がしています。積極的に外の人たちと関わっていきたいと思っています。オフィスも落ち着いた雰囲気の場所にありますから、多くの皆さんにもきていただきたいですね。<br>
<br>
また、今後は、フレックス制度や、休暇の取りやすい制度もうまく利用して、プライベートの時間も充実させていきたいと思います。旅行と映画鑑賞が趣味なのですが、ここのところ行けてなかったので、海外旅行に行きたいと思っています。中でも、メキシコのカンクンはぜひとも行ってみたいところです。</p>
<p class="interview-photo"><img src="img/interview-photo4.jpg" alt=""></p>
</section>


