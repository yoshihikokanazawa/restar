<article id="interview-area">


<section>
<h2 class="ttl-blue-underline">中国の大学ではソフトウェアを専攻。上流工程からできるエンジニアを目指して日本へ。</h2>
<p class="interview-txt">出身は中国です。大学でソフトウェアを学んだ後、日本へ来ました。大学のあった大連は、ソフトウェアの業界では、日本のプロジェクトのコーディング業務などを行う、オフショア開発が盛んでした。中国でプログラマーとして働く道もありましたが、大連で働くのであれば、将来的には日本語が不可欠になります。「日本語に加え、プロジェクト全体を管理するための知識を得られれば、より上の仕事ができる」という想いがありました。<br>
<br>
日本に来た当時はひらがなも知りませんでしたので、日本語学校に2年間通いました。その後、日本の大学院でマネジメント工学を学んだことにより、システム開発の要件定義や設計から、プログラミングまで、広い視点を持つことができたと思います。<br>
<br>
大学院卒業後は日系企業に就職しましたが、ソフトウェアとは関係のない部署に配属になり、業務内容もルーチンワークで成長が感じづらい環境でした。「中国語、韓国語を話せる」という強みも発揮できず、「もっと語学力をいかせる環境で働きたい」という気持ちが膨らんでいったんです。</p>
</section>


<section>
<h2 class="ttl-blue-underline">当社へ入社。中国赴任中は、技術の知識をいかして活躍。</h2>
<p class="interview-txt">転職活動を経て、フィールドアプリケーションエンジニア（FAE）として当社へ入社しました。台湾系の民生用タッチパネルの担当となり、入社後、すぐに工場のある中国の深圳に配属されました。「中国の工場と日本のお客様をつなげる」という自分が希望していた仕事に就くことができ、やりがいを感じることができました。<br>
<br>
中国の会社や工場との交渉時は、通訳を使わずに現場の人と直接会話できますし、その場で他部署の担当者やサプライヤーを紹介してもらうこともできます。スピード感をもって、会社としての意図や背景、業務の優先順位などを詳しく伝えられます<br>
<br>
中国と日本では「文化の違い」もあります。日本では依頼時に「～できたらいいのですが」などと、曖昧に伝えることもありますよね。中国では「～してください」とハッキリ伝えた方が、先方もプロジェクトが進めやすいんです。そうした細かい部分でもスピードが違ってきます。</p>
<p class="interview-photo"><img src="img/interview-photo1.jpg" alt=""></p>
</section>


<section>
<h2 class="ttl-blue-underline">車載用液晶ディスプレイがメイン商材。今後の目標はソリューションとしての提案。</h2>
<p class="interview-txt">現在は東京にあるレスターエレクトロニクス第３営業部門に在籍し、中国の大手メーカーによる、車載用の液晶ディスプレイをメインに扱っています。主な仕事は企画の立ち上げから仕様決め、製品の単価、納期管理など。中国の工場や社内の技術部とも相談し、試作を重ねながら量産に向けて進めていきます。<br>
<br>
技術的な理解も必要ですし、中国メーカーを担当しているため、自分の強みもいかせています。今後は、車載関係の他の商材についても知識を高めて、ソリューションとしてお客様に提案できる営業を目指したいですね。液晶ディスプレイ以外のシステムやICと一緒に提案することで付加価値が高まり、ターゲットも広がります。お客様からのニーズを吸い上げて、他の商材についての知識を深めていきたいです。</p>
</section>


<section>
<h2 class="ttl-blue-underline">レスターグループの強みは商材の豊富さ。中国市場の開拓にさらなる可能性が。</h2>
<p class="interview-txt">レスターグループ全体の強みを挙げると、扱っている商材の豊富さ。新しいソリューションの提案も、商材が豊富だからこそ実現できること。アジアを中心に海外にも多くの拠点があり、中国でも深圳、上海、北京、重慶など、拠点が複数あるため、現地の工場やサプライヤーともリレーションを深められます。<br>
<br>
現在は海外にも設計の場が移ってきているため、海外での営業の機会が増えています。中国は市場も大きいですし、今後もビジネスチャンスがありそうです。外国籍社員の活躍の場は、これからも増えていくと思います。</p>
<p class="interview-photo"><img src="img/interview-photo2.jpg" alt=""></p>
</section>


<section>
<h2 class="ttl-blue-underline">働きやすい職場環境。フレックス制をいかし、子供の送迎をすることも。</h2>
<p class="interview-txt">レスターエレクトロニクスは10時～15時のコアタイム勤務のため、フレックスで出社できるのも魅力。実は、深圳へは単身赴任をしていました。日本に戻ってきたのは子供が生まれたから。幼稚園への送迎や子供が体調を崩したときなども、フレックス制のおかげで早退や遅刻にならないのでとても助かっています。上司や仲間にサポートしてもらいながら、働くことができているのがありがたいですね。まだ子供が小さいので、子供と過ごす時間も大切にしています。<br>
<br>
社内の人間関係もいいですし、先輩や後輩とも気さくに話せます。最近は、会社の仲間に誘われて、ゴルフをはじめました。プライベートでも部署を越えて色々な人と交流できるのが楽しいですね。深圳での単身赴任時も、日本からの出張者と一緒に食事をとるなど、現地にいながら社内のつながりを作ることができました。ゴルフで、さらに人間関係が広がったと感じています。</p>
<p class="interview-photo"><img src="img/interview-photo3.jpg" alt=""></p>
</section>


