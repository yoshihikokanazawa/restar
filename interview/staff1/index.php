<section>
<h2 class="ttl-blue-underline">幅広い電気機器に関わることに魅力を感じ、商社へ。</h2>
<p class="interview-txt">大学卒業後、当社に入社しました。学生時代から、カメラや携帯電話、テレビなどの電気機器が好きで、それらに関わる業界に進みたいと考えていました。商社を選んだのは、身近な電機メーカーはもちろん、ほかにもたくさんのメーカーや製造元の方々と接する機会があるからです。あらゆるメーカー、製造元の方々と関わり、幅広いジャンルの商材を扱っていることが、商社の魅力だと思います。<br>
<br>
入社後はプリンター向け半導体のセールス業務を行っていました。その後、情報通信やインフラ系のお客様相手のセールス担当を経て、大手メーカーのデバイス営業部門に出向しました。<br>
<br>
現在はレスターエレクトロニクスで営業として、工場の生産ラインなどで使用されるカメラ向けに、半導体を提案する業務をしています。さらに、レスターマーケティングにも所属しており、マーケット調査や、社内への販売促進のアドバイスなどのプロダクトマーケティング業務を行っています。<br>
<br>
私は営業とマーケティングを兼務していますが、当社では営業と他部門の兼務をすることは珍しくありません。新しく学ぶことも多いですし、色々な経験ができ、やりがいを感じています。</p>
<p class="interview-photo"><img src="img/interview-photo1.jpg" alt=""></p>
</section>


<section>
<h2 class="ttl-blue-underline">大手メーカーへの出向経験により、視野が広がった。</h2>
<p class="interview-txt">これまでのキャリアの中でとくに印象深いのは、仕入先である大手メーカーへの3年間の出向です。出向時は、デジタルカメラに使用されているイメージセンサーの営業とマーケティングを行っていました。<br>
<br>
帰任したのは、ちょうどレスターグループが誕生した2019年です。出向したことで、仕入先が「会社としてどのようなビジョンを持っているのか」、「ひとつの商材に対して、どのようなプロセスを経て、商品設計や価格決定を行い、販売戦略を描くのか」、を知ることができました。また、「商社には何が求められているのか」を体感することができ、非常に勉強になりました。<br>
<br>
現在の業務にも、出向の経験が大きく役立っています。例えば、仕入先との交渉を行う際に、お客様がビジネスを大きく成長させたいと思っている領域であれば、その可能性をうまく伝えることで、お互いにとってよりよい方向に交渉が進められます。出向以前にも意識していたことですが、実際に仕入先の現場を知ることで理解が深まり、交渉をよりスムーズに進められるようになりました。<br>
<br>
出向した仕入先は海外にも広く事業展開していたため、そのマーケットの広がりを目の当たりにし、見識を深めることができました。仕入先の方々と一緒に働いたことで、連帯感も生まれたと思いますし、取り扱っていたデジタルカメラを個人的に購入したこともあります。今後の業務でも、出向で得た知識や経験をいかしていきたいです。</p>
<p class="interview-photo"><img src="img/interview-photo2.jpg" alt=""></p>
</section>


<section>
<h2 class="ttl-blue-underline">フラットな雰囲気で、自由にチャレンジできる環境が強み。</h2>
<p class="interview-txt">当社の強みのひとつが、普段の勤務の中でも、上司や部下といった役職や年齢にとらわれず、フラットなコミュニケーションがとれること。統合によりレスターグループが誕生してから、とくにその傾向が強まったようにも感じています。これは個人的な推測ですが、レスターグループという新体制に入るにあたり、上司が社員同士で相談やサポートをしやすい雰囲気づくりをしている部分もあるのかもしれません。<br>
<br>
そういった話しやすい雰囲気があるため、若手でもチャレンジもしやすい環境であるのも強みです。業務の中でも、新しい提案は活発に行われていますし、目的や効果を明確にすれば、自分のやりたいことが実現できるチャンスがあります。既存のフォーマットにしばられず、自由な発想が可能です。私自身もお客様へ新しい提案を行っています。<br>
<br>
働きやすさという点では、フレックス制度があるのもいいですね。私は子供を保育所に送った後、朝8時半頃には出社していますが、会社としてのコアタイムは10時～15時なので、社員それぞれの家庭環境やライフスタイルに合わせて柔軟に働くことができます。<br>
<br>
今後の目標は、海外で新しい経験を積むことです。レスターグループは中国やシンガポールなど、海外にも拠点を持っていますし、海外メーカーともつながりがありますので。プライベートでは、スポーツなど、子供と一緒に楽しめることを見つけたいですね。</p>
