<h2 class="ttl-blue-underline">共創の広がり。いま、求められる技術とは。</h2>

<h3 class="heading-blue">―レスターグループが目指す、技術の展望とは。</h3>
<dl>
<dt>レスターエレクトロニクス　清水</dt>
<dd class="interview-txt">「システム/プラットフォーム提案への変革」を目指し、プラットフォームを作るビジネスでは、プロジェクトの開発や要件定義を把握する為に、最終ユーザーである「上流工程」から考えていく必要があります。今までは、主に半導体デバイスを扱ってきましたので、どちらかというとプロジェクトの中では「中流、下流工程」のスキームでやってきていましたが、共創パートナーやPCIグループの協力も借りて上流顧客へのアプローチをしていかなくてはいけません。<br><br>最近の事例では、AI関連においても弊社グループ企業含め共創パートナーと組み連携した検証が進んでいます。<br>エレクトロニクス業界の背景を考えると、上流の顧客と関係のあるグループ企業や共創パートナーと組む事で、私達もそういった上流顧客の現場の実態、困りごとを把握し、課題を解決するためにAI含めシステム的にサービスを提供していくことが必要になります。</dd>
</dl>

