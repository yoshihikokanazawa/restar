<dd class="interview-txt">私達、レスターグループは主に「半導体商社」として長年ビジネスを行ってきました。ですが、近年、お客様の開発スパンが短くなり、エンジニアの開発工数不足もそれに重なり、「限られた設計リソースの中で、半導体だけを単品で売る」ビジネスでは、ご満足いただけない時代に変わりつつあります。<br>
<br>
そういった背景を踏まえて、私達、レスターエレクトロニクスは半導体デバイス・電子部品の単品販売から脱却し、「システム/プラットフォーム提案への変革」を目指しています。
つまり、「半導体」を部品としてだけ売るのではなく、当社で扱う様々な部品を組み合わせていく。さらに、そこにはソフトウェアも搭載していきます。一例としてお客様が使用される状態により近い「システムボード」を開発し、こちらからそれらをご提案することで、商社としての付加価値を上げていきたいと考えています。<br>
<br>
ソフトウェア開発の部分ではPCIグループに協力いただいているのをはじめ、グループ会社や他社との“共創”による実現を目指しています。</dd>
</dl>


<h3 class="heading-blue">―“共創”がもたらす相乗効果について教えてください。</h3>


<dl>
