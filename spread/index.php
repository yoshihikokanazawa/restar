<h3 class="heading-blue">―共創は今後も広がっていくのでしょうか。</h3>
<dl>
<dt>レスターエレクトロニクス　清水</dt>
<dd class="interview-txt">当社だけでは、お客様が求めるニーズに完璧に応えられる訳ではありません。取り扱うカテゴリもそうですが、現在のサービスだけではご提供できないものもある。お客様のご要望に応えるためにも、共創は更に広がっていくと思います。</dd>
</dl>

<h3 class="heading-blue">―共創に関して、力を入れていきたい分野はありますか。</h3>
<dl>
<dt>レスターエレクトロニクス　清水</dt>
<dd class="interview-txt">AIの他、5Ｇ（第5世代移動通信システム）に注目が集まっている背景を考えると、通信系の分野ですね。同時に、通信分野のエンジニアを強化していくことも必要と考えています。</dd>
<dt>プリバテック/PCI　須藤</dt>
<dd class="interview-txt">AIは手段なんです。例えば、いまは工場で人間が目視で行う作業を、AIのエンジンとカメラを組ませることで、人間が行うよりもはるかに作業効率を上げることができます。当然、作業工程の中には、AIでは対応できず、人間でなければ対応できない作業も出てきます。様々な手段を広範囲で見て、お客様のご要望に対して、最適な実現方法を考えられる人材が求められるのではないでしょうか。</dd>
<dt>レスターエレクトロニクス　清水</dt>
<dd class="interview-txt">そういった意味では、技術の知識を持った上で、コンサルティングができる人が求められています。膨大な情報をもとにビジネス戦略を提案するという意味では、データサイエンティスト的な人材ニーズは高いと思います。</dd>
</dl>

