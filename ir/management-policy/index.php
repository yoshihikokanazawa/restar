<article id="ir-risk">
<h2 class="ttl-navy">事業等のリスク</h2>


<p>当社グループの財政状態及び経営成績に影響を及ぼす可能性のあるリスクには以下のようなものがあります。なお、文中における将来に関する事項は、2019年3月期決算発表日において判断したものであり、また、事業等のリスクはこれらに限定されるものではりません。</p>


<h3>①競合についてのリスク</h3>
<p>当社グループが属するエレクトロニクス業界は、「市場の成熟化と新興企業の参入」、「ＩｏＴ／ＡＩの進展」、「ニーズの多様化・高度化」といった大きな環境変化の中にあり、競争が非常に激しい業界であります。そのため、価格競争の激化、技術革新に伴う商品の陳腐化といった要因が当社グループの業績に影響を及ぼす可能性があります。</p>


<h3>②海外進出に伴うリスク</h3>
<p>当社グループは、海外各国・地域で事業を展開しております。そのため、関連する海外各国・地域における政治・経済状況の変化、法律・税制の変化、テロ・戦争等による社会的混乱、債権回収リスク、労働力不足・人件費高騰等が当社グループの業績に影響を及ぼす可能性があります。</p>


<h3>③為替変動の影響についてのリスク</h3>
<p>当社グループは、日本国内の他、海外各国・地域でグローバルな事業活動を展開しているため、為替変動の影響を受けております。このため、為替予約等によるリスクヘッジを行っておりますが、為替リスクを完全に払拭することは困難であり、著しい為替変動は当社グループの業績に影響を及ぼす可能性があります。</p>


<h3>④新規事業の立ち上げに伴うリスク</h3>
<p>当社グループが持続的な成長を遂げ、世界市場を視野に入れたエレクトロニクス総合商社を目指すためには、新たなビジネスや革新的なサービスの創出、多様な事業展開を図ることが重要であると認識しております。新たなビジネスの立ち上げにあたっては、その市場性や採算性などについて十分な検証を行った上で意思決定を行っておりますが、市場環境の急激な変化や不測の事態等により当初の事業計画を達成できない場合には、当社グループの業績及び財務状況に影響を及ぼす可能性があります。</p>


<h3>⑤技術投資に関するリスク</h3>
<p>当社グループが属するエレクトロニクス業界は、技術革新や事業環境の変化のスピードが極めて速く、必要となる投資額も増加しております。その投資額に見合ったリターンを得られないような場合は、当社グループの業績と財務状況に影響を及ぼす可能性があります。</p>


<h3>⑥人材の確保及び育成についてのリスク</h3>
<p>当社グループが今後も持続的な成長を遂げ、世界市場を視野に入れたエレクトロニクス総合商社を目指すためには、優れたスキル・ノウハウを保有した人材の採用及び育成が重要であると認識しております。こういった人材を確保または育成できなかった場合には、当社グループの事業展開や業績に影響を及ぼす可能性があります。</p>


<h3>⑦自然災害・事故災害等のリスク</h3>
<p>当社グループは、多様な事業を展開しており、地震・洪水・台風等の自然災害や火災等の事故災害が発生した場合、自社の関連施設が直接的な被害を受けるリスクに加えて、仕入先メーカー・顧客メーカー等の操業停止に伴い、当社グループの事業活動が停滞し、当社グループの業績に影響を及ぼす可能性があります。</p>


<h3>⑧当社グループが展開する事業に関するリスク</h3>
<div>

<h4>・顧客の需要動向に関するリスク</h4>
<p>当社グループが販売する半導体及び電子部品は、顧客（セットメーカー）製品に搭載され、機器は顧客の業務プロセスの一部に組み込まれ、使用されています。そのため、顧客製品の需要動　向・搭載機能や経済環境・景気の変動に伴う顧客の設備投資動向に変更が生じた場合、当社グループの業績に影響を及ぼす可能性があります。</p>

<h4>・顧客及び仕入先に関するリスク</h4>
<p>当社グループは国内外の多岐に渡る企業と取引を行っております。半導体及び電子部品事業及び調達事業では、特定の企業の部品を多く取り扱っているため、当該企業への依存度が高くなっており、当該企業の経営方針の変更等が当社グループの業績に影響を及ぼす可能性があります。また、仕入先の事業再編（Ｍ＆Ａ等）や販売チャネル・テリトリー政策の見直し等により、当社グループの商権に変更が生じた場合にも、当社グループの業績と財務状況に影響を及ぼす可能性があります。</p>

<h4>・事業の季節要因についてのリスク</h4>
<p>当社グループが展開する電子機器事業は、放送関連市場、企業・学校・官公庁市場を主な市場としている特性から、顧客の予算執行の関係で期末の３月と９月に売上高が集中する傾向にあります。そのため、期末月の売上予測を過大に見積もった場合、当社グループの業績予測に影響を及ぼす可能性があります。</p>

<h4>・施策の変更等に関するリスク</h4>
<p>当社グループが展開する環境エネルギー事業は、国の施策や環境規制等の様々な法令・規制との関連性が高い面を有しております。そのため、国の施策や環境規制等に変更があった場合、当社グループの業績に影響を及ぼす可能性があります。</p>

<h4>・電力市場価格の変動に関するリスク</h4>
<p>原油価格の上昇や為替の大幅な変動、また自然災害等による原子力発電所の稼働停止等の要因で、電力の市場価格が大きく変動することにより、新電力事業の採算性に影響を与える可能性があり、その場合は当社グループの業績に影響を及ぼす可能性があります。</p>

<h4>・植物工場事業に関するリスク</h4>
<p>植物工場内において、設備の故障や植物の病気、害虫が発生した場合は、工場の操業を一時停止し問題を解決する必要があり、生産計画に影響を与える可能性があります。また、提供する商品の不具合の発生等において第三者から費用請求等を受け、その責任が当社グループに起因するものと判断された場合には、当社グループの業績に影響を及ぼす可能性があります。</p>

